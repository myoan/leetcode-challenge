func Test_filter(t *testing.T) {
	testcases := []struct {
		msg    string
		input  []int
		expect []int
	}{
		{
			"simple",
			[]int{1},
			[]int{1},
		},
		{
			"len is 3",
			[]int{1, 2, 3},
			[]int{1, 2, 3},
		},
		{
			"same number exists 2",
			[]int{1, 2, 2, 3},
			[]int{1, 2, 2, 3},
		},
		{
			"same number exists 3",
			[]int{1, 2, 2, 2, 3},
			[]int{1, 2, 2, 2, 3},
		},
		{
			"same number exists 4",
			[]int{1, 2, 2, 2, 2, 3},
			[]int{1, 2, 2, 2, 3},
		},
		{
			"same number exists at last",
			[]int{1, 2, 2, 2, 2, 2},
			[]int{1, 2, 2, 2},
		},
		{
			"same number exists at first",
			[]int{2, 2, 2, 2, 2, 4},
			[]int{2, 2, 2, 4},
		},
	}

	for _, tt := range testcases {
		actual := threeSum_filter(tt.input)
		if !matchIntArray(actual, tt.expect) {
			t.Errorf("[%s] act: '%v', exp: '%v'\n", tt.msg, actual, tt.expect)
		}
	}
}

